     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'name' => 'required|max:225|unique:categories',
        ]);
    
        $slug = str_replace(' ', '-',$request->name);

        Category::create([
            'name'=> $request->name,
            'slug'=>  Str::lower($slug),
            ]
        );

        return redirect('/dashboard/category')->with('successAdd', 'negro');
    }

